        // ==================== SCROLL SUAVE Y NAVEGACIÓN ACTIVA ====================
        document.addEventListener('DOMContentLoaded', function() {
            // Scroll suave para los enlaces de navegación
            document.querySelectorAll('a[href^="#"]').forEach(enlace => {
                enlace.addEventListener('click', function(e) {
                    e.preventDefault();
                    const destino = document.querySelector(this.getAttribute('href'));
                    if (destino) {
                        destino.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });

            // Actualizar enlace activo en la navegación al hacer scroll
            const secciones = document.querySelectorAll('section');
            const enlacesNav = document.querySelectorAll('.navbar-nav .nav-link');

            window.addEventListener('scroll', function() {
                let actual = '';
                
                secciones.forEach(seccion => {
                    const topSeccion = seccion.offsetTop;
                    const alturaSeccion = seccion.clientHeight;
                    if (window.pageYOffset >= topSeccion - 100) {
                        actual = seccion.getAttribute('id');
                    }
                });

                enlacesNav.forEach(enlace => {
                    enlace.classList.remove('active');
                    if (enlace.getAttribute('href') === `#${actual}`) {
                        enlace.classList.add('active');
                    }
                });
            });

            // Cerrar panel al hacer clic fuera
            document.addEventListener('click', function(evento) {
                const panel = document.getElementById('panelConfig');
                const boton = document.querySelector('.boton-configuracion');
                
                if (panel.classList.contains('activo') && 
                    !panel.contains(evento.target) && 
                    !boton.contains(evento.target)) {
                    panel.classList.remove('activo');
                }
            });

            // Cargar preferencias guardadas
            const temaGuardado = localStorage.getItem('tema-preferido');
            if (temaGuardado) {
                document.body.setAttribute('data-tema', temaGuardado);
                document.getElementById('selectorTema').value = temaGuardado;
            }

            const idiomaGuardado = localStorage.getItem('idioma-preferido');
            if (idiomaGuardado) {
                document.getElementById('selectorIdioma').value = idiomaGuardado;
                cambiarIdioma(document.getElementById('selectorIdioma'));
            }

            const tamanoGuardado = localStorage.getItem('tamano-fuente');
            if (tamanoGuardado) {
                document.documentElement.style.setProperty('--tamano-fuente-base', tamanoGuardado);
                document.getElementById('tamanoFuente').value = tamanoGuardado;
            }

            // Animación de barras de progreso
            const observador = new IntersectionObserver((entradas) => {
                entradas.forEach(entrada => {
                    if (entrada.isIntersecting) {
                        const barras = entrada.target.querySelectorAll('.progreso-relleno');
                        barras.forEach(barra => {
                            const ancho = barra.style.width;
                            barra.style.width = '0';
                            setTimeout(() => {
                                barra.style.width = ancho;
                            }, 100);
                        });
                    }
                });
            }, { threshold: 0.5 });

            const seccionHabilidades = document.getElementById('habilidades');
            if (seccionHabilidades) {
                observador.observe(seccionHabilidades);
            }
        });

