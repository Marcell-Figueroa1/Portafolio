        // Initialize
        window.addEventListener('load', () => {
            setTimeout(() => {
                document.getElementById('loader').classList.add('hidden');
            }, 1000);

            // Load saved preferences
            const savedLang = localStorage.getItem('language') || 'es';
            const savedTheme = localStorage.getItem('theme') || 'default';
            const savedFontSize = localStorage.getItem('fontSize') || '16';

            changeLanguage(savedLang);
            changeTheme(savedTheme);
            changeFontSize(savedFontSize);

            document.getElementById('langSelect').value = savedLang;
            document.getElementById('fontSizeSlider').value = savedFontSize;

            // Scroll animations
            const observerOptions = {
                threshold: 0.1,
                rootMargin: '0px 0px -100px 0px'
            };

            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('active');
                    }
                });
            }, observerOptions);

            document.querySelectorAll('.scroll-animate').forEach(el => {
                observer.observe(el);
            });
        });

        // Toggle settings sidebar
        function toggleSettings() {
            const sidebar = document.getElementById('settingsSidebar');
            sidebar.classList.toggle('active');
        }

        // Change language
        function changeLanguage(lang) {
            localStorage.setItem('language', lang);
            const elements = document.querySelectorAll('[data-i18n]');
            
            elements.forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (translations[lang] && translations[lang][key]) {
                    el.textContent = translations[lang][key];
                }
            });
        }

        // Change theme
        function changeTheme(themeName) {
            localStorage.setItem('theme', themeName);
            const theme = themes[themeName];
            const root = document.documentElement;

            root.style.setProperty('--primary-color', theme.primary);
            root.style.setProperty('--secondary-color', theme.secondary);
            root.style.setProperty('--accent-color', theme.accent);
            root.style.setProperty('--bg-primary', theme.bgPrimary);
            root.style.setProperty('--bg-secondary', theme.bgSecondary);
            root.style.setProperty('--bg-tertiary', theme.bgTertiary);
            root.style.setProperty('--text-primary', theme.textPrimary);
            root.style.setProperty('--text-secondary', theme.textSecondary);

            const gradient = `linear-gradient(135deg, ${theme.primary}, ${theme.secondary}, ${theme.accent})`;
            root.style.setProperty('--gradient', gradient);

            // Update active theme button
            document.querySelectorAll('.theme-btn').forEach(btn => {
                btn.classList.remove('active');
            });
            document.querySelector(`[data-theme="${themeName}"]`).classList.add('active');
        }

        // Change font size
        function changeFontSize(size) {
            localStorage.setItem('fontSize', size);
            document.body.style.fontSize = size + 'px';
            document.getElementById('fontSizeValue').textContent = size + 'px';
        }

        // Parallax effect
        document.addEventListener('mousemove', (e) => {
            const shapes = document.querySelectorAll('.floating-shape');
            const x = e.clientX / window.innerWidth;
            const y = e.clientY / window.innerHeight;

            shapes.forEach((shape, index) => {
                const speed = (index + 1) * 50;
                const xMove = (x - 0.5) * speed;
                const yMove = (y - 0.5) * speed;
                shape.style.transform = `translate(${xMove}px, ${yMove}px)`;
            });
        });

        // Smooth scroll for navigation
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });

        // Close settings when clicking outside
        document.addEventListener('click', (e) => {
            const sidebar = document.getElementById('settingsSidebar');
            const settingsBtn = document.querySelector('.settings-btn');
            
            if (!sidebar.contains(e.target) && !settingsBtn.contains(e.target)) {
                sidebar.classList.remove('active');
            }
        });