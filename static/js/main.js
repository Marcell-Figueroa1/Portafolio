        // ==================== FUNCIÓN TOGGLE PANEL ====================
        function togglePanel(elemento) {
            const panel = document.getElementById('panelConfig');
            panel.classList.toggle('activo');
        }

        // ==================== FUNCIÓN CERRAR PANEL ====================
        function cerrarPanel(elemento) {
            const panel = document.getElementById('panelConfig');
            panel.classList.remove('activo');
        }

        // ==================== FUNCIÓN CAMBIAR TEMA ====================
        function cambiarTema(selector) {
            const tema = selector.value;
            document.body.setAttribute('data-tema', tema);
            // Guardar preferencia en localStorage
            localStorage.setItem('tema-preferido', tema);
        }

        // ==================== FUNCIÓN CAMBIAR IDIOMA ====================
        function cambiarIdioma(selector) {
            const idioma = selector.value;
            const textos = traducciones[idioma];
            
            // Cambiar todos los elementos con data-traducir
            document.querySelectorAll('[data-traducir]').forEach(elemento => {
                const clave = elemento.getAttribute('data-traducir');
                if (textos[clave]) {
                    elemento.textContent = textos[clave];
                }
            });

            // Cambiar placeholders
            document.querySelectorAll('[data-traducir-placeholder]').forEach(elemento => {
                const clave = elemento.getAttribute('data-traducir-placeholder');
                if (textos[clave]) {
                    elemento.placeholder = textos[clave];
                }
            });

            // Guardar preferencia
            localStorage.setItem('idioma-preferido', idioma);
        }

        // ==================== FUNCIÓN CAMBIAR TAMAÑO FUENTE ====================
        function cambiarTamanoFuente(selector) {
            const tamano = selector.value;
            document.documentElement.style.setProperty('--tamano-fuente-base', tamano);
            // Guardar preferencia
            localStorage.setItem('tamano-fuente', tamano);
        }

        // ==================== FUNCIÓN EFECTO HOVER BOTÓN ====================
        function efectoHoverBoton(elemento, entrando) {
            if (entrando) {
                elemento.style.backgroundColor = '#f3f4f6';
                elemento.style.transform = 'scale(1.05)';
            } else {
                elemento.style.backgroundColor = 'white';
                elemento.style.transform = 'scale(1)';
            }
        }

        // ==================== FUNCIÓN EFECTO HOVER CARD ====================
        function efectoHoverCard(elemento) {
            elemento.style.borderColor = 'var(--color-primario)';
            elemento.style.transform = 'translateY(-8px)';
        }

        // ==================== FUNCIÓN EFECTO SALIR CARD ====================
        function efectoSalirCard(elemento) {
            elemento.style.borderColor = 'var(--color-borde)';
            elemento.style.transform = 'translateY(0)';
        }

